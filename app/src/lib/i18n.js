export function normalizeLanguage(language) {
  return language === "zh-Hant" ? "zh-Hant" : "en";
}

export function getTranslations(language) {
  const normalizedLanguage = normalizeLanguage(language);
  const dictionary = dictionaries[normalizedLanguage] || dictionaries.en;

  if (normalizedLanguage === "zh-Hant") {
    return {
      ...dictionaries.en,
      ...dictionary,
      monthly: dictionary.recurringMonthly || "每月",
      nonMonthly: "非每月",
    };
  }

  return {
    ...dictionaries.en,
    ...dictionary,
  };
}
